package it.uniroma2.art.semanticturkey.alignment;

public class InvalidAlignmentRelationException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -1250401799867030082L;

	public InvalidAlignmentRelationException() {
		super();
	}

	public InvalidAlignmentRelationException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidAlignmentRelationException(String message) {
		super(message);
	}

	public InvalidAlignmentRelationException(Throwable cause) {
		super(cause);
	}

}
