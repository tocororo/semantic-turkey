package it.uniroma2.art.semanticturkey.alignment;

public class AlignmentInitializationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4459950090079076588L;
	
	public AlignmentInitializationException() {
		super();
	}

	public AlignmentInitializationException(String message, Throwable cause) {
		super(message, cause);
	}

	public AlignmentInitializationException(String message) {
		super(message);
	}

	public AlignmentInitializationException(Throwable cause) {
		super(cause);
	}

}
