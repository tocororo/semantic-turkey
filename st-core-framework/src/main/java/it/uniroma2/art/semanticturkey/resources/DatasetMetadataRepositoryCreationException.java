package it.uniroma2.art.semanticturkey.resources;

public class DatasetMetadataRepositoryCreationException extends DatasetMetadataRepositoryException {

	private static final long serialVersionUID = 1065067048834964363L;

	public DatasetMetadataRepositoryCreationException() {
		super();
	}

	public DatasetMetadataRepositoryCreationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DatasetMetadataRepositoryCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public DatasetMetadataRepositoryCreationException(String message) {
		super(message);
	}

	public DatasetMetadataRepositoryCreationException(Throwable cause) {
		super(cause);
	}

}
