package it.uniroma2.art.semanticturkey.exceptions;

public class UnsupportedModelException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8779432906082047713L;
	
	public UnsupportedModelException() {
		super();
	}
	
	public UnsupportedModelException(String msg) {
		super(msg);
	}
	
	public UnsupportedModelException(Throwable e) {
		super(e);
	}

}
