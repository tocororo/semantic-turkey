package it.uniroma2.art.semanticturkey.mdr.core;

public class MetadataRegistryIntializationException extends MetadataRegistryException {

	private static final long serialVersionUID = 1065067048834964363L;

	public MetadataRegistryIntializationException() {
		super();
	}

	public MetadataRegistryIntializationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MetadataRegistryIntializationException(String message, Throwable cause) {
		super(message, cause);
	}

	public MetadataRegistryIntializationException(String message) {
		super(message);
	}

	public MetadataRegistryIntializationException(Throwable cause) {
		super(cause);
	}

}
